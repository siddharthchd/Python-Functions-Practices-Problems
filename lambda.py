# def square(num) : return num ** 2
# This can be done with lambda expression used once (anonymous function)

square = lambda num: num ** 2
print(square(5))

mynums = [1,2,3,4,5,6,7,8,9]
my_map_list  = list(map(lambda num : num ** 2, mynums))
print(my_map_list)

my_filter_list = list(filter(lambda num: num % 2 == 0, mynums))
print(my_filter_list)